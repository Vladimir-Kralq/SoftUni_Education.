function genderAndAge (age,gender) {
if(gender==="m" && age>=16)
{
    console.log("Mr.");
    
}
else if (gender==="m" && age<16)
{
    console.log("Master");
    
}
else if (gender==="f" && age>=16)
{
    console.log("Ms.");
    
}
else if (gender==="f" && age<16)
{
    console.log("Miss");
    
}







}

genderAndAge(12,"f");
